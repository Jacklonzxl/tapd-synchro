package com.ext.tapd.tapd.pojo;

/**
 * @author lx
 */
public class StatusCounter {
    private int pass;
    private int no_pass;
    private int block;
    private int unexecuted;

    public int getPass() {
        return pass;
    }

    public void setPass(int pass) {
        this.pass = pass;
    }

    public int getNo_pass() {
        return no_pass;
    }

    public void setNo_pass(int no_pass) {
        this.no_pass = no_pass;
    }

    public int getBlock() {
        return block;
    }

    public void setBlock(int block) {
        this.block = block;
    }

    public int getUnexecuted() {
        return unexecuted;
    }

    public void setUnexecuted(int unexecuted) {
        this.unexecuted = unexecuted;
    }
}
